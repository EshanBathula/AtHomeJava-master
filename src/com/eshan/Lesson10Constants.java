package com.eshan;

public class Lesson10Constants {
    public static void main(String[] args){
        final float PI = 3.14F;//add F to represent variable as a float
        //add final to beginning to make java recognize the variable as a constant
        //use capital letters when initializing constants
        //PI = 2; wont work since PI is a constant
        System.out.println(PI);
    }
}
